package br.com.sestren.vraptormultimodule.web.config;

public class Admin {

	private String url;
	private String token;

	public Admin() {
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
}
